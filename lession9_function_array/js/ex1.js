console.log('Các số lẻ từ 5 đến 70');
    function oddNumber(){
        for (let i = 5; i <= 70; i+=2){
            console.log(i);
        }
    }
    oddNumber();

console.log('Các số chẵn từ 70 đến 5');
    function evenNumber(){
        for (let i = 70; i >= 5; i-=2){
            console.log(i);
        }
    }
    evenNumber();